"""Message format presets — instructs the LLM how to structure replies."""

from __future__ import annotations

FORMAT_INSTRUCTIONS: dict[str, str] = {
    "text": (
        "Формат сообщений — только текст. Никогда не предлагай кнопки, "
        "не используй markdown. Перечисляй варианты через запятую или с новой "
        "строки тире."
    ),
    "buttons": (
        "Формат сообщений — везде где можно, давай выбор кнопками. "
        "Это особенно важно для: выбора услуги, выбора даты/времени, "
        "подтверждения записи. В тексте сообщения коротко описывай ситуацию, "
        "а варианты — кнопками. (Реальные кнопки рендерит фронтенд по "
        "extracted_data, тебе достаточно явно перечислить варианты.)"
    ),
    "hybrid": (
        "Формат сообщений — гибрид: используй кнопки для типовых выборов "
        "(услуга, дата, время, «да/нет/другое»), а свободный текст — для "
        "уточняющих вопросов и пояснений. Если не уверен — пиши текстом."
    ),
}


def format_block(message_format: str) -> str:
    return FORMAT_INSTRUCTIONS.get(message_format, FORMAT_INSTRUCTIONS["hybrid"])
